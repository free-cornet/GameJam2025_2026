<script>
export default {
  data() {
    return {
      accepted: false,
      acceptedTooEarly: false,
      acceptedTooFast: false,
      hasScrolledToBottom: false,
      hasScrolled: false,
      timer: null,
      countdown: 10,
      // Reduce scroll speed
      scrollSpeed: 0.3
    }
  },
  mounted() {
    const scrollBox = this.$refs.termsContainer;
    if (scrollBox) {
        scrollBox.addEventListener('wheel', this.handleWheel, { passive: false });
    }
    },
beforeUnmount() {
    const scrollBox = this.$refs.termsContainer;
    if (scrollBox) {
        scrollBox.removeEventListener('wheel', this.handleWheel);
    }
    this.stopTimer();
    },
  methods: {
    startTimer() {
        this.timer = setInterval(() => {
            this.countdown--;
            
            if (this.countdown <= 0) {
            this.stopTimer();
            }
        }, 1000);
    },

    stopTimer() {  
      if (this.timer) {
        clearInterval(this.timer);
        this.timer = null;
      }
    },

    checkScrollPosition(event) {
      const element = event.target;
      const threshold = 10; // pixels from bottom to trigger
      const isAtBottom = element.scrollHeight - element.scrollTop - element.clientHeight < threshold;
      
      if (!this.hasScrolled) {
        this.startTimer();
        this.hasScrolled = true;
      }
      
      if (isAtBottom) {
        this.hasScrolledToBottom = true;
      }
    },

    checkAcceptedTooEarly(event) {
        // event.preventDefault();
        const scrollBox = this.$refs.termsContainer;
        if (!this.hasScrolledToBottom) {
            event.preventDefault();
            this.acceptedTooEarly = true; 
            this.accepted = false;
            this.hasScrolled = false;
            this.hasScrolledToBottom = false;
            this.stopTimer();
            this.countdown = 10;
            this.acceptedTooFast = false;
            scrollBox.scrollTop = 0;
        }
        else if (!this.acceptedTooFast && this.countdown > 0) {
            event.preventDefault();
            this.accepted = false;
            this.hasScrolledToBottom = false;
            this.hasScrolled = false;
            this.acceptedTooEarly = false;
            this.stopTimer();
            this.countdown = 10;
            this.acceptedTooFast = true;
            scrollBox.scrollTop = 0;
        } else {
            this.accepted = !this.accepted;
            event.target.checked = this.accepted;
        }

        
    },
    acceptTerms() {
      if (this.accepted) {
        // Handle acceptance
        navigateTo("/game");
      }
    }
  },
  beforeUnmount() {
    this.stopTimer();  // Clean up timer when component is destroyed
  }
}
</script>

